package sept.sept23.poly.MOLoading;

public class Person {

    void Speak(){
        System.out.println("No Argument");
    }

    void Speak(int i){
        System.out.println("Yes Int -> " + i);
    }

    void Speak(String i){
        System.out.println("Yes String -> " + i);
    }

    void Speak(double i){
        System.out.println("Yes Double -> " + i);
    }

}
